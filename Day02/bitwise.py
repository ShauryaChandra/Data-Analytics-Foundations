#bitwise operators

print(bin(10)) #to convert to binar

a = 10
b = 8
print(a & b) #AND operator
print(a | b) #OR operator
print(a ^ b) #XOR operator

print(10 >> 1)  # Right shift: shifts bits 1 place to the right (10 becomes 5)
print(1 << 10)  # Left shift: shifts bits 10 places to the left (1 becomes 1024)