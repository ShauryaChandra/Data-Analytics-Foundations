a = ["Hulk", "Iron Man", "Vision", "Thor"]

# to create a copy of a list
b = a.copy()
print(b)

# to access an element
print(a.index("Thor"))

# to extend the list
c = ["Spiderman", "Wanda"]
a.extend(c)
print(a)

# to reverse the list
a.reverse()
print(a)

# to sort the list
a.sort()
print(a)

# to clear the data from the list
a.clear()
print(a)